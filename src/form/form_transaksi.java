    private void jtdibayarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtdibayarKeyPressed
        try {
            if(evt.getKeyCode() == KeyEvent.VK_ENTER){
                int totalharga = Integer.parseInt(jttotalharga.getText());
                int dibayar = Integer.parseInt(jtdibayar.getText());
                
                int kembalian = dibayar - totalharga;
                jtkembalian.setText(Integer.toString(kembalian));
                
                if(dibayar == totalharga){
                    lbstatus.setText("LUNAS");
                }else if(dibayar < totalharga){
                    lbstatus.setText("BELUM LUNAS");
                    jtkembalian.setText("0");
                }else if(dibayar > totalharga){
                    lbstatus.setText("LUNAS");
                }
                jbsimpan.doClick();
                
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Tolong Isi Field!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jtdibayarKeyPressed

