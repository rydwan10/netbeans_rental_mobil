    private void jtlamasewaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtlamasewaKeyPressed
        try {
            if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            
            int harga_sewa = Integer.parseInt(jthargasewa.getText());
            int lamasewa = Integer.parseInt(jtlamasewa.getText());
        
            int harga = harga_sewa * lamasewa;
        
        jtharga.setText(Integer.toString(harga));
        jtpotongan.requestFocus();
        
        }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Tolong Isi Field!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jtlamasewaKeyPressed

